// Tutorial: Cube Mapping (Skybox)
// In this lesson we will learn how to use a 3D texture to texture a sphere. This technique is called cube
// mapping, and we will learn how to make a skybox using this technique.
// Cube Mapping is a technique often used to create a surrounding environment infinitely far away, such as
// the sky. We can create a sky box using this technique, by loading a 3D texture, then using this 3d image to
// texture a sphere that surrounds the camera. To keep this illusion up, the skybox must always be centered
// around the camera, so no matter how far in any direction we move, we will never get closer to the skybox.
// Another thing is the skybox is always in the back of the depth buffer, so everything drawn onto the screen
// will ALWAYS be in front of the skybox. We can do this by setting the z value in our view space to 1. We will
// learn how to create a cube texture using the directx texture tool. A cube texture is six images, two for each
// axis(one for + x, -x, +y, -y, +z, and -z).
// To map the "cube" texture onto a sphere, its actually not too difficult. All we have to do is take the position 
// of the current pixel we are rendering, and turn it into a vector to find which texel from the cube map we will 
// be coloring the pixel. The below diagram shows you the idea in 2D, which we would be using (u,v) texture 
// coordinates, but in 3D, the idea is the same, but instead of the 2D (u,v) texture coordinates, we will be using 
// 3D (u,v,w) texture coordinates.
// I used a program called Terragen to create these pictures. Very cool program. two things i should mention though, 
// is first, all six of the images must be the exact same dimensions, such as 1024x1024. also if you do decide to 
// use terragen to create cube maps, you might have a problem with "seams" between each picture, this is usually 
// caused by "global illumination" or GI. you will need to turn both the GI settings to zero in the render window 
// if you are having problems with seams.
// Alright, Now, I'll teach you how to create a 3D texture using the directx texture tool.
// Step 1 - Create 6 pictures representing the different sides of a cube; East(+X), West(-X), Up(+Y), Down(-Y),
// North(+Z), South(-Z) using a program such as terragen.
// Step 2 - Open the DirectX texture tool(usually by going to start->all programs->microsoft directx SDK->DirectX 
// Utilities->DirectX Texture Tool)
// Step 3 - Go to File - New Texture...
// Step 4 - A window should pop up.under the first section, where it says texture type, mark Cubemap Texture.
// Change the second part to the x and y dimensions of your textures(remember all six of the images must be the 
// same dimensions). Then you can change to format to a more compressed type if you'd like since these files can 
// get pretty large. Hit OK.
// Step 5 - Go to View->Cube Map Face-> and choose one of the faces(you will eventually choose all faces). Then go 
// to File->Open Onto This Cubemap Face and choose the image corresponding with the current direction you chose
// (if you chose to view Positive X, then you will need to load the image in the East direction). Do this for all 
// six directions.
// Step 6 - After you have done that, save the file, and thats it.
// Now that we have all the preparation done, lets move on to the code
